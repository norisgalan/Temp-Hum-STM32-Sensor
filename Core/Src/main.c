/**
* @brief Function implementing the Task_Sensor thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartTask02 */
void StartTask02(void const * argument)
{
  DHT22_Data local_data;
  for(;;)
  {
    HAL_GPIO_WritePin(GPIOG, GPIO_PIN_14, GPIO_PIN_SET);

    // Citim o singură dată
    uint8_t status = DHT22_Read(&local_data);
    global_status = status; // Salvăm statusul pentru LCD

    if(status == 0)
    {
        global_dht_data = local_data;
        HAL_GPIO_TogglePin(GPIOG, GPIO_PIN_13);
    }

    HAL_GPIO_WritePin(GPIOG, GPIO_PIN_14, GPIO_PIN_RESET);
    osDelay(2000);
  }
}

